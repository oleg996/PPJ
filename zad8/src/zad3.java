public class zad3 {
    public static void main(String[] args) {
        System.out.println("while (i<10) with 10 ");
        int i = 10;
        while (i<10){

            System.out.println(i);
            i++;

        }
        System.out.println("do-while (i<10) with 10");
        i = 10;
        do{

            System.out.println(i);
            i++;

        } while (i<10);

    }

}
